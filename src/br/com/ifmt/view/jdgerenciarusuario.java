    public void iniciaComboBox() {
        DefaultComboBoxModel<String> dcm = (DefaultComboBoxModel<String>) jCBNivel.getModel();
        dcm.addElement("ESCOLHA UM NIVEL");
        for (Funcao funcaoEnum : Funcao.values()) {
            dcm.addElement(funcaoEnum.getDescricao());
        }
    }

